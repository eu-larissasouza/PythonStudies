# Até o momento, estávamos desenvolvendo de um segmento do back-end de um aplicativo
# chamado Sabor Express, que está, atualmente, realizando o cadastro de restaurantes.

# Estávamos utilizando funções, métodos e estruturas sequenciais em nosso código, agora,
# vamos adentrar no paradigma orientado a objetos, que nos ajudará a garantir um pouco
# mais como conseguimos lidar com os dados na nossa aplicação.

# Quando temos uma classe e queremos criar um restaurante novo, falamos que estamos instanciando,
# ou criando, um objeto, sendo que um objeto é a instância de uma classe.

# Então, para criarmos um restaurante novo, primeiro criaremos uma variável.

# Por exemplo, criaremos a variável de restaurante_praca, que será igual ao objeto Restaurante().


# De agora em diante, qualquer restaurante que quisermos criar, teremos o nome de uma variável,
# onde vamos armazená-lo, e informaremos que ele será do tipo, Restaurante().

# Sempre que pensarmos em uma classe, temos que pensar quais são os atributos,
# ou seja, as características dessa entidade que queremos criar e assim, teremos
# um objeto do tipo da classe criada, mas ainda não vimos como ver as informações
# presentes nos atributos em nossos objetos

# Existe uma maneira de conseguirmos visualizar o que a classe Restaurante tem como propriedades,
# atributos e métodos de um objeto, que é a função dir(). Por outro lado, No entanto, existe outra
# função que exibirá um dicionário com os atributos dessa classe, a função vars().

